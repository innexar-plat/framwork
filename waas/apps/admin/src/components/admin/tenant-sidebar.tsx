export function TenantSidebar() {
  const { t } = useI18n();
  const pathname = usePathname();
  const { me } = useAuth();
  const enabledModules = me?.enabled_modules ?? [];

  const items = TENANT_NAV.filter((item) => {
    if (!item.module) return true;
    return enabledModules.includes(item.module);
  });

  return (
    <aside
      className="flex w-60 flex-col border-r border-border sidebar-glass"
      aria-label="Tenant sidebar"
    >
      {/* Tenant brand */}
      <div className="flex h-16 items-center gap-3 border-b border-border px-5">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-accent/10">
          <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" className="text-accent" aria-hidden>
            <circle cx="12" cy="12" r="10" />
            <path d="M8 14s1.5 2 4 2 4-2 4-2" />
            <line x1="9" y1="9" x2="9.01" y2="9" />
            <line x1="15" y1="9" x2="15.01" y2="9" />
          </svg>
        </div>
        <div className="min-w-0">
          <p className="text-sm font-bold text-primary truncate tracking-tight">
            {me?.tenant ? me.tenant.name : "WaaS"}
          </p>
          <p className="text-xs text-muted truncate">Workspace</p>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex flex-1 flex-col gap-1 overflow-y-auto p-3" aria-label="Main">
        {items.map(({ href, labelKey, icon }) => {
          const isActive =
            pathname === href ||
            (href !== "/" && pathname.startsWith(href));
          return (
            <Link
              key={href}
              href={href}
              className={`nav-item ${
                isActive ? "nav-item-active" : "nav-item-inactive"
              }`}
              aria-current={isActive ? "page" : undefined}
            >
              <span className="shrink-0 [&>svg]:text-current">
                {icon}
              </span>
              {t(labelKey)}
            </Link>
          );
        })}
      </nav>

      {/* Bottom */}
      <div className="border-t border-border p-4">
        <p className="text-xs text-muted">Powered by WaaS</p>
      </div>
    </aside>
  );
}
